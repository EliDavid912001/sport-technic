'use strict';
/**
 * One-off generator: merges git HEAD exerciseRegistry into supplemental catalog + heritage.
 * Run: node scripts/generate-exercise-supplement.js
 */
const fs = require('fs');
const path = require('path');

const oldPath = path.join(__dirname, '..', '.tmp-old-registry.js');
if (!fs.existsSync(oldPath)) {
  console.error('Missing .tmp-old-registry.js — run git show HEAD:src/data/exerciseRegistry.js > .tmp-old-registry.js');
  process.exit(1);
}
const old = require(oldPath);

/** Old registry keys superseded by canonical snake_case ids (do not duplicate). */
const SUPERSEDED_OLD_KEYS = new Set([
  'muscleup', 'pullup', 'chinup', 'inclinepress', 'row', 'rdl', 'curl', 'tricep',
  'lateralraise', 'pullover', 'cablefly', 'facepull', 'latpulldown', 'chestpressmachine',
  'legpress', 'hacksquat', 'smithsquat', 'rearfly', 'seatedcalfraise', 'calfraise'
]);

/** equipment_type for legacy exercises not in the canonical 20 list */
const EQUIPMENT_BY_OLD_KEY = {
  overheadpress: 'barbell',
  dips: 'bodyweight',
  pushup: 'bodyweight',
  benchpress: 'barbell',
  legextension: 'machine',
  legcurl: 'machine',
  seatedrowmachine: 'machine',
  shoulderpressmachine: 'machine',
  cablepushdown: 'cable',
  cablecurl: 'cable',
  dumbbellrow: 'dumbbell',
  dumbbellpress: 'dumbbell',
  seateddumbbellpress: 'dumbbell',
  squat: 'barbell',
  lunges: 'dumbbell',
  deadlift: 'barbell',
  hipthrust: 'barbell'
};

function anatomyToTargetMuscles(anatomy) {
  const line = (ref) => `${ref.nameHe} (${ref.nameEn})`;
  return {
    primary: (anatomy.primary || []).map(line),
    secondary: (anatomy.secondary || []).map(line),
    stabilizers: (anatomy.stabilizers || []).map(line)
  };
}

function heritageFromOldEntry(entry) {
  const meta = entry.metadata || {};
  const cv = entry.cv_profile || {};
  const h = {
    name_en: (meta.name && meta.name.en) || entry.key,
    icon: meta.icon || '🏋️',
    mode: meta.mode || 'unknown',
    biomechanical_cues: entry.biomechanical_cues || []
  };
  if (cv && cv.category) {
    h.cv_profile = {
      category: cv.category,
      primary_tracking_joints: cv.primary_tracking_joints,
      movement_axis: cv.movement_axis,
      motion: cv.motion
    };
  }
  if (entry.scoring_rules) h.scoring_rules = entry.scoring_rules;
  return h;
}

const supplementalCatalog = [];
const supplementalHeritage = {};

for (const [oldKey, entry] of Object.entries(old.exerciseRegistry)) {
  if (SUPERSEDED_OLD_KEYS.has(oldKey)) continue;
  const equipment_type = EQUIPMENT_BY_OLD_KEY[oldKey] || 'machine';
  const meta = entry.metadata || {};
  supplementalCatalog.push({
    id: oldKey,
    name_he: (meta.name && meta.name.he) || oldKey,
    equipment_type,
    target_muscles: anatomyToTargetMuscles(entry.anatomy || { primary: [], secondary: [], stabilizers: [] })
  });
  supplementalHeritage[oldKey] = heritageFromOldEntry(entry);
}

// Also pull heritage for superseded keys into canonical ids (handled in main file manually for canonical 20)

const outPath = path.join(__dirname, '..', 'src', 'data', 'exerciseSupplement.generated.js');
const body = `'use strict';
/** AUTO-GENERATED by scripts/generate-exercise-supplement.js — legacy exercises restored */
const SUPPLEMENTAL_CATALOG = ${JSON.stringify(supplementalCatalog, null, 2)};

const SUPPLEMENTAL_HERITAGE = ${JSON.stringify(supplementalHeritage, null, 2)};

module.exports = { SUPPLEMENTAL_CATALOG, SUPPLEMENTAL_HERITAGE };
`;
fs.writeFileSync(outPath, body, 'utf8');
console.log('Wrote', outPath, 'entries:', supplementalCatalog.length);
